package yamlImport;

public class Project {
        private String name;
        private String description;

    public Project() {
    }

    public String getName() {
            return name;
        }

        public void setName(String value) {
            this.name = value;
        }

        public String getDescription() {
            return description;
        }

        public void setDescription(String value) {
            this.description = value;
        }
    }
